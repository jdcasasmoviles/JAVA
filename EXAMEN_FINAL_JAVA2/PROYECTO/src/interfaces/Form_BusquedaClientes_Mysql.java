    public void limpiar_tabla(JTable tabla){
    for (int x = 0; x < tabla.getRowCount(); x++){
        tabla.setValueAt("",x,0);
        tabla.setValueAt("",x,1);
        tabla.setValueAt("",x,2);
        tabla.setValueAt("",x,3);
        tabla.setValueAt("",x,4);
    }
    }
                    
            
           
    
