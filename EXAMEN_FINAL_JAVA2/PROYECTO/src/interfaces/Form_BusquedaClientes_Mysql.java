    private void btnBuscarEmpresaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarEmpresaActionPerformed
        // TODO add your handling code here:
        String dato = txtEmpresa.getText();
        ResultSet data = lib.consulta("select Codigo,Empresa,Ruc,Telefono,Contacto from clientes where Empresa='"+dato+"'");

        try{
            limpiar_tabla(tablaResultados);
            int fila=0;
            while(data.next()){
                for(int i=1; i<=lib.num_campos; i++){
                    tablaResultados.setValueAt(data.getString(1),fila,0);
                    tablaResultados.setValueAt(data.getString(2),fila,1);
                    tablaResultados.setValueAt(data.getString(3),fila,2);
                    tablaResultados.setValueAt(data.getString(4),fila,3);
                    tablaResultados.setValueAt(data.getString(5),fila,4);

                }
                fila++;
            }

        }catch(SQLException ex){
        }
    }//GEN-LAST:event_btnBuscarEmpresaActionPerformed

