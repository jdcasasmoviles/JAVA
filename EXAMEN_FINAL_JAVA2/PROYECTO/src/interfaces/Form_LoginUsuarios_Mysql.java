    public void validar(){
    if(usuario.equals("")){
    JOptionPane.showMessageDialog(null,"ERROR...Usuario sin datos ","Validacion de datos",
    JOptionPane.WARNING_MESSAGE);txtUsuario.requestFocus();
    }else{
         lib.validar_usuario(usuario,clave,email);
         if(lib.error1==1){
         JOptionPane.showMessageDialog(null, "ERROR...Usuario no existe","Validacion de datos",
         JOptionPane.WARNING_MESSAGE);
         txtUsuario.requestFocus();
         }else{
              if(lib.error2==1){
              JOptionPane.showMessageDialog(null, "ERROR...Clave errada","Validacion de datos",
              JOptionPane.WARNING_MESSAGE);
              txtPassword.requestFocus();
              }else{
                     if(lib.error3==1){
                     JOptionPane.showMessageDialog(null, "ERROR...e-mail errado","Validacion de datos",
                     JOptionPane.WARNING_MESSAGE);
                     txtEmail.requestFocus();
                     }
                     else{
                     JOptionPane.showMessageDialog(null, "CORRECTO...Usuario,email y Password","Validacion de datos",
                     JOptionPane.WARNING_MESSAGE);
                     setVisible(false);
                     Menu obj3=new Menu();
                    obj3.setVisible(true);
                    }
                }
            }
        }    
    }
    
